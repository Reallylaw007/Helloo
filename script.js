function renderQuestion() {
    const q = questions[currentQuestionIndex];
    const progress = (currentQuestionIndex / questions.length) * 100;

    let optionsHtml = '';
    const prefixes = ['ก.', 'ข.', 'ค.', 'ง.'];
    q.options.forEach((opt, index) => {
        // String.raw to safely escape strings inside onclick
        optionsHtml += `<button class="option-btn" onclick="selectOption('${opt.type}')"><span style="font-weight: bold; color: var(--accent); margin-right: 8px;">${prefixes[index]}</span>${opt.text}</button>`;
    });

    appContainer.innerHTML = `
        <div class="fade-in" id="quiz-box">
            <div class="progress-container">
                <div class="progress-bar" style="width: ${progress}%" id="progress"></div>
            </div>
            <span class="question-number">ข้อ ${currentQuestionIndex + 1} จาก ${questions.length}</span>
            <h2 class="question-text">${q.q}</h2>
            
            <div class="options-container">
                ${optionsHtml}
            </div>
        </div>
    `;
    
    setTimeout(() => {
        document.getElementById('progress').style.width = `${((currentQuestionIndex + 1) / questions.length) * 100}%`;
    }, 50);
}

function selectOption(type) {
    if (type === 'M') scoreM++;
    else scoreU++;

    const quizBox = document.getElementById('quiz-box');
    quizBox.classList.remove('fade-in');
    quizBox.classList.add('fade-out');

    setTimeout(() => {
        currentQuestionIndex++;
        if (currentQuestionIndex < questions.length) {
            renderQuestion();
        } else {
            renderResult();
        }
    }, 300);
}

